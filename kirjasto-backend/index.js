const typeDefs = `
  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    id: ID!
    born: Int
    bookCount: Int!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`

const resolvers = {
  Query: {
    bookCount: async () => Book.collection.countDocuments(),
    authorCount: async () => Author.collection.countDocuments(),
    allBooks: async (root, args) => {
      const books = await Book.find({})
      return books.filter((book) => (args.genre ? book.genres.includes(args.genre) : true));

      // const byGenre = (book) =>
      //   !args.genre || Book.collection.find({ genres: args.genre }) ? 
      // return Book.find({})

      // filters missing
      // const byAuthor = (book) =>
      //   !args.author || args.author === book.author ? book : !book
      // const byGenre = (book) =>
      //   !args.genre || book.genres.find(p => p === args.genre) ? book : !book
      // return books.filter(byAuthor).filter(byGenre)
    },
    allAuthors: async () => {
      return Author.find({})
    }
  },
  Author: {
    bookCount: async (root) => {
      let bookCount = 0
      for (let i = 0; i < books.length; i++) {
        if (root.name === books[i].author) {
          bookCount++
        }
      }
      return bookCount
    }
  },
  Mutation: {
    addBook: async (root, args) => {
      const author = await Author.findOne({ name: args.author })
      const book = await new Book({ author: author._id, title: args.title, published: args.published, genres: args.genres })
      return book.save()
      // if (!authors.find(p => p.name === args.author)) {
      //   const author = { name: args.author, id: uuid() }
      //   authors = authors.concat(author)
      // }
      // const book = { ...args, id: uuid() }
      // books = books.concat(book)
      // return book
    },
    editAuthor: async (root, args) => {
      const author = await Author.findOne({ name: args.name })
      author.born = args.setBornTo
      return author.save()
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
